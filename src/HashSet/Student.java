package HashSet;

public class Student {
    private String name;
    private int not;

    public Student(String name, int not) {
        this.name = name;
        this.not = not;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getNot() {
        return not;
    }

    public void setNot(int not) {
        this.not = not;
    }
}
